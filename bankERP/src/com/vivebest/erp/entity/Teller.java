package com.vivebest.erp.entity;

import java.util.Date;

public class Teller {
	private int tId; 			//柜员id
	private long tNumber;		//柜员工号
	private String tName;		//柜员姓名
	private String tPassword;	//柜员密码
	private int autId;			//权限id
	private Date tLoginTime;	//最近登录时间
	private boolean isDelete;	//软删除
	
	
	
	public Teller() {
		super();
	}

	
	
	public Teller(int tId, long tNumber, String tName, String tPassword,
			int autId, Date tLoginTime, boolean isDelete) {
		super();
		this.tId = tId;
		this.tNumber = tNumber;
		this.tName = tName;
		this.tPassword = tPassword;
		this.autId = autId;
		this.tLoginTime = tLoginTime;
		this.isDelete = isDelete;
	}

	public int gettId() {
		return tId;
	}

	public void settId(int tId) {
		this.tId = tId;
	}

	public long gettNumber() {
		return tNumber;
	}

	public void settNumber(long tNumber) {
		this.tNumber = tNumber;
	}

	public String gettName() {
		return tName;
	}

	public void settName(String tName) {
		this.tName = tName;
	}

	public String gettPassword() {
		return tPassword;
	}

	public void settPassword(String tPassword) {
		this.tPassword = tPassword;
	}

	public int getAutId() {
		return autId;
	}

	public void setAutId(int autId) {
		this.autId = autId;
	}

	public Date gettLoginTime() {
		return tLoginTime;
	}

	public void settLoginTime(Date tLoginTime) {
		this.tLoginTime = tLoginTime;
	}


	
	
	public boolean getIsDelete() {
		return isDelete;
	}



	public void setDelete(boolean isDelete) {
		this.isDelete = isDelete;
	}



	@Override
	public String toString() {
		return "Teller [tId=" + tId + ", tNumber=" + tNumber + ", tName="
				+ tName + ", tPassword=" + tPassword + ", autId=" + autId
				+ ", tLoginTime=" + tLoginTime + ", isDelete=" + isDelete + "]";
	}
	
	
	
	
}
