package com.vivebest.erp.entity;

public class Authority {
	private int autId; // 权限id
	private String autContent;// 权限内容
	private boolean isDelete;// 软删除



	

	public int getAutId() {
		return autId;
	}





	public void setAutId(int autId) {
		this.autId = autId;
	}





	public String getAutContent() {
		return autContent;
	}





	public void setAutContent(String autContent) {
		this.autContent = autContent;
	}





	public boolean isDelete() {
		return isDelete;
	}





	public void setDelete(boolean isDelete) {
		this.isDelete = isDelete;
	}





	@Override
	public String toString() {
		return "Authority [autId=" + autId + ", autContent=" + autContent + ",isDelete="+isDelete+"]";
	}

}
